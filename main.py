def pluse2(nums):
    result = []
    for num in nums:
        result.append(num + 2)
    return result

def mulpitly2(nums):
    result = []
    for num in nums:
        result.append(num * 2)
    return result

def exponent2(nums):
    result = []
    for num in nums:
        result.append(num ** 2)
    return result